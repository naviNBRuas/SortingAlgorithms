from typing import Sequence, Optional
from ..base import _setup_tracer, _finalize, SortTracer, Number


def cycle_sort(data: Sequence[Number], *, trace: bool = False, tracer: Optional[SortTracer] = None):
    arr = list(data)
    tracer, ret_trace = _setup_tracer("cycle_sort", trace, tracer)
    n = len(arr)
    for cycle_start in range(0, n - 1):
        item = arr[cycle_start]
        pos = cycle_start
        for i in range(cycle_start + 1, n):
            if arr[i] < item:
                pos += 1
        if pos == cycle_start:
            continue
        while item == arr[pos]:
            pos += 1
        arr[pos], item = item, arr[pos]
        if tracer:
            tracer.record(arr, (cycle_start, pos), "place")
        while pos != cycle_start:
            pos = cycle_start
            for i in range(cycle_start + 1, n):
                if arr[i] < item:
                    pos += 1
            while item == arr[pos]:
                pos += 1
            arr[pos], item = item, arr[pos]
            if tracer:
                tracer.record(arr, (cycle_start, pos), "cycle")
    return _finalize(arr, tracer, ret_trace)
